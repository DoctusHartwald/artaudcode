package com.myshop.customerclient.io;

public class OrderDispatcherException extends Exception {

	public OrderDispatcherException() {
		super();
	}

	public OrderDispatcherException(String message, Throwable cause) {
		super(message, cause);
	}

	public OrderDispatcherException(String message) {
		super(message);
	}

	public OrderDispatcherException(Throwable cause) {
		super(cause);
	}

}
